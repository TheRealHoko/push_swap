#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	lens;

	if (!dst || !src)
		return (0);
	lens = ft_strlen(src);
	if (lens + 1 < size)
		ft_memcpy(dst, src, lens + 1);
	else if (size != 0)
	{
		ft_memcpy(dst, src, size - 1);
		dst[size - 1] = '\0';
	}
	return (lens);
}
